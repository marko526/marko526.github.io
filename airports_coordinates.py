def _collect_codes(airport: Airport) -> Tuple[str, ...]:
    return tuple({airport.iata.upper(), airport.icao.upper(), *[alias.upper() for alias in airport.aliases]})


def _build_coordinate_index(entries: Iterable[Airport]) -> Dict[str, Tuple[float, float]]:
    mapping: Dict[str, Tuple[float, float]] = {}
    for airport in entries:
        for code in _collect_codes(airport):
            mapping[code] = airport.coordinates
    return mapping


def _build_airport_lookup(entries: Iterable[Airport]) -> Dict[str, Airport]:
    lookup: Dict[str, Airport] = {}
    for airport in entries:
        for code in _collect_codes(airport):
            lookup[code] = airport
    return lookup


def _build_suggestions(entries: Iterable[Airport]) -> List[Dict[str, str]]:
    suggestions: List[Dict[str, str]] = []
    for airport in entries:
        suggestions.append(
            {
                "label": airport.display_label,
                "value": airport.preferred_code,
            }
        )
    return sorted(suggestions, key=lambda item: item["label"])


